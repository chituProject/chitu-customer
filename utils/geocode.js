export function regionNameToAreaCode(name) {
  if (!name || typeof name !== 'string') return null;
  const areas = name.split(/\s/);
  let areaCode;
  for (let i = areas.length; i > 0; i -= 1) {
    areaCode = geoMap[areas[i]];
    if (areaCode) {
      return areaCode;
    }
  }
  return geoMap['其他'];
}

